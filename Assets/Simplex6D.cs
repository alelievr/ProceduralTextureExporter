	const uint FNV_32_PRIME = ((uint)0x01000193);
	const uint FNV_32_INIT = ((uint)2166136261);
	const uint FNV_MASK_8 = (((uint)1<<8)-1);

	static float F4 = (Mathf.Sqrt(7.0f)-1.0f)/6.0f; //(Mathf.Sqrt(5.0f)-1.0f)/4.0f;

    // Unskew
    // self.g=self.f/(1+self.d*self.f)
    static float G4 = F4/(1.0f+6.0f*F4);

    static float sideLength=Mathf.Sqrt(6.0f)/ (6.0f * F4 +1.0f);
    static float a=Mathf.Sqrt((sideLength*sideLength)-((sideLength/2.0f)*(sideLength/2.0f)));
    static float cornerFace=Mathf.Sqrt(a*a + (a/2.0f)*(a/2.0f));

    static float cornerFaceSqrd=cornerFace*cornerFace;

	static int fast_floor(float t)
	{
		return (t > 0 ? (int)t : (int)t - 1);
	}

	static uint fnv_32_a_buf(uint[] buf, uint len)
	{
		uint hval = (uint)FNV_32_INIT;
		for (int i = 0; i < buf.Length; i++)
		{
			hval ^= buf[i];
			hval *= FNV_32_PRIME;
		}
		return hval;
	}


	static byte xor_fold_hash(uint hash)
	{
		// Implement XOR-folding to reduce from 32 to 8-bit hash
		return (byte)((hash >> 8) ^ (hash & FNV_MASK_8));
	}

	static uint hash_coords_6(int x, int y, int z, int w, int u, int v, uint seed)
	{
		uint[] d = new uint[] { (uint)x, (uint)y, (uint)z, (uint)w, (uint)u, (uint)v, seed };
		return xor_fold_hash(fnv_32_a_buf(d, 7));
	}

	struct SVectorOrdering
	{
		SVectorOrdering(double v, int a)
		{
			val = v;
			axis = a;
		}

		public double val;
		public int axis;
	}

	static void sortBy_6(float[] l1, int[] l2)
	{
		SVectorOrdering[] a = new SVectorOrdering[6];
		for (int c = 0; c < 6; ++c) { a[c].val = l1[c]; a[c].axis = l2[c]; }
		Array.Sort(a, (v1, v2) => v1.val.CompareTo(v2.val));
		for (int c = 0; c < 6; ++c) l2[c] = a[c].axis;
	}

	public static float GetValue(float x, float y, float z, float w, float u, float v, uint seed)
	{
		// Skew
		//self.f = ((self.d + 1) ** .5 - 1) / self.d

		//self.valueScaler=(self.d-1)**-.5
		float valueScaler = Mathf.Pow(5.0f, -0.5f);
		valueScaler *= Mathf.Pow(5.0f, -3.5f) * 100 + 13;

		float[] loc = new float[]{ x, y, z, w, u, v };
		float s = 0;
		for (int c = 0; c < 6; ++c) s += loc[c];
		s *= F4;

		int[] skewLoc = { fast_floor(x + s), fast_floor(y + s), fast_floor(z + s), fast_floor(w + s), fast_floor(u + s), fast_floor(v + s) };
		int[] intLoc = { fast_floor(x + s), fast_floor(y + s), fast_floor(z + s), fast_floor(w + s), fast_floor(u + s), fast_floor(v + s) };
		float unskew = 0.0f;
		for (int c = 0; c < 6; ++c) unskew += skewLoc[c];
		unskew *= G4;
		float[] cellDist ={loc[0]-(float)skewLoc[0]+unskew, loc[1]-(float)skewLoc[1]+unskew,
		loc[2]-(float)skewLoc[2]+unskew, loc[3]-(float)skewLoc[3]+unskew,
		loc[4]-(float)skewLoc[4]+unskew, loc[5]-(float)skewLoc[5]+unskew};
		int[] distOrder = { 0, 1, 2, 3, 4, 5 };
		sortBy_6(cellDist, distOrder);

		int[] newDistOrder = { -1, distOrder[0], distOrder[1], distOrder[2], distOrder[3], distOrder[4], distOrder[5] };

		float n = 0.0f;
		float skewOffset = 0.0f;

		for (int c = 0; c < 7; ++c)
		{
			int i = newDistOrder[c];
			if (i != -1) intLoc[i] += 1;

			float[] u2 = new float[6];
			for (int d = 0; d < 6; ++d)
			{
				u2[d] = cellDist[d] - (intLoc[d] - skewLoc[d]) + skewOffset;
			}

			float t = cornerFaceSqrd;

			for (int d = 0; d < 6; ++d)
			{
				t -= u2[d] * u2[d];
			}

			if (t > 0.0f)
			{
				uint h = hash_coords_6(intLoc[0], intLoc[1], intLoc[2], intLoc[3], intLoc[4], intLoc[5], seed);
				float[] vec = gradient6D_lut[h];
				float gr = 0.0f;
				for (int d = 0; d < 6; ++d)
				{
					gr += vec[d] * u2[d];
				}

				n += gr * t * t * t * t;
			}
			skewOffset += G4;
		}
		n *= valueScaler;
		return n;
	}

